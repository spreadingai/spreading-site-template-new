export interface DocuoConfig {
  title: string;
  description?: string;
  favicon: string;
  /**
   * 对这些前缀开头的 href，不做 docId 转换/小写化，保持 MDX 里写的原始大小写。
   * 常用于由 Nginx/外部系统接管的路径（例如 /unique-api、/client-api）。
   */
  passthroughPrefixes?: string[];
  logo:
    | string
    | {
        dark?: string;
        light?: string;
      };
  url?: string;
  metadata?: Record<string, string>[];
  instances: DocInstance[];
  themeConfig: {
    [key: `navbar${string}`]: NavbarConfig;
    [key: `footer${string}`]: FooterConfig;
    colorMode?: ColorMode;
    colors?: Colors;
    removeWatermark?: boolean;
    showAskAI?: boolean;
  };
  search?: {
    hidden?: boolean;
    algolia?: {
      appId: string;
      apiKey: string;
      indexName: string;
    };
  };
  analytics?: {
    ga4?: {
      measurementId: string;
    };
    baidu?: {
      tongjiID?: string;
    };
  };
  i18n?: {
    defaultLocale: string;
    localeConfigs: {
      [index: string]: string;
    };
  };
  sitemap?: {
    exclude?: string[];
    siteUrl?: string;
  };
  redirects?: RedirectConfig[];
}

